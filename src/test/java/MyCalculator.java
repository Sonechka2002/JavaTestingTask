public class MyCalculator {

    // Метод для обычного теста: просто складывает два числа
    public static int sum(int a, int b) {
        return a + b;
    }

    // Метод для параметризованного теста: проверяет, больше ли число десяти
    public static boolean isGreaterThanTen(int number) {
        return number > 10;
    }
}

